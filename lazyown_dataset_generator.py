_GENERIC_VERBS = {
    "execute", "run", "perform", "do", "carry", "conduct", "apply",
    "invoke", "call", "use", "trigger",
}


def _is_noisy_phrasing(instruction: str, arg: str) -> bool:
    """
    Return True if the phrasing is too generic and likely to dilute training.
    Criteria:
      - empty arg AND the instruction starts with a generic verb
      - instruction has fewer than 4 meaningful tokens
    """
    words = instruction.split()
    if len(words) < 4:
        return True
    if not arg.strip():
        first_word = words[0].lower().rstrip(",;:!?.")
        if first_word in _GENERIC_VERBS:
            return True
    return False


