def _apply_pentest_synonyms(instr: str) -> str:
    """Replace common pentest terms with synonyms to increase diversity."""
    for synonym_group in _PENTEST_SYNONYMS:
        for word in synonym_group:
            if word in instr.lower():
                # Pick a different synonym from the same group
                others = [s for s in synonym_group if s.lower() != word.lower()]
                if others:
                    new_word = random.choice(others)
                    # Case-insensitive replace, preserving case of first letter
                    pattern = re.compile(re.escape(word), re.IGNORECASE)
                    instr = pattern.sub(new_word, instr, count=1)
                    break
    return instr


def _expand(tool_name: str, phrasings: List[Tuple[str, str]]) -> List[Tuple[str, str]]:
    """Generate additional phrasings via IP substitution, prefix injection, verb swap, and synonym replacement."""
    result = list(phrasings)
    seen = {p[0].lower() for p in phrasings}

    # 1. IP substitution — multiple substitutions per source phrasing
    for instr, arg in phrasings:
        subs_made = 0
        for old_ip, new_ip in _IP_SUBS:
            if old_ip in instr or old_ip in arg:
                new_instr = instr.replace(old_ip, new_ip)
                new_arg = arg.replace(old_ip, new_ip)
                if new_instr.lower() not in seen:
                    seen.add(new_instr.lower())
                    result.append((new_instr, new_arg))
                    subs_made += 1
                    if subs_made >= 3:
                        break

    # 2. Prefix injection — on short imperative phrasings starting with an action verb
    _ACTION_VERBS = {
        "run", "show", "get", "list", "check", "generate", "create", "add",
        "set", "start", "stop", "read", "search", "find", "execute", "display",
        "launch", "spawn", "inject", "update", "build", "export", "poll",
        "broadcast", "send", "scan", "enumerate", "analyze", "deploy",
        "dump", "extract", "harvest", "task", "delegate", "recommend",
        "acknowledge", "mark", "clear", "prune", "recover", "plan",
    }
    imperatives = [
        (i, a) for i, a in phrasings
        if len(i) <= 70 and "→" not in i and "—" not in i
        and (i.split()[0].lower() if i.split() else "") in _ACTION_VERBS
    ]
    random.shuffle(imperatives)
    en_used = es_used = pt_used = hack_used = 0
    for instr, arg in imperatives:
        verb = instr.split()[0]
        rest = instr[len(verb):].lstrip()
        lowered_instr = verb.lower() + (" " + rest if rest else "")
        if en_used < 5:
            new_instr = _PREFIXES_EN[en_used % len(_PREFIXES_EN)].format(lowered_instr)
            if new_instr.lower() not in seen:
                seen.add(new_instr.lower())
                result.append((new_instr, arg))
                en_used += 1
        if es_used < 3:
            new_instr = _PREFIXES_ES[es_used % len(_PREFIXES_ES)].format(lowered_instr)
            if new_instr.lower() not in seen:
                seen.add(new_instr.lower())
                result.append((new_instr, arg))
                es_used += 1
        if pt_used < 2:
            new_instr = _PREFIXES_PT[pt_used % len(_PREFIXES_PT)].format(lowered_instr)
            if new_instr.lower() not in seen:
                seen.add(new_instr.lower())
                result.append((new_instr, arg))
                pt_used += 1
        if hack_used < 2:
            new_instr = _PREFIXES_HACKER[hack_used % len(_PREFIXES_HACKER)].format(lowered_instr)
            if new_instr.lower() not in seen:
                seen.add(new_instr.lower())
                result.append((new_instr, arg))
                hack_used += 1

    # 3. Verb synonym swap (Run→Execute, Show→Display, etc.)
    for instr, arg in phrasings:
        for old_v, new_v in _CMD_VERBS:
            if instr.startswith(old_v):
                new_instr = new_v + instr[len(old_v):]
                if new_instr.lower() not in seen:
                    seen.add(new_instr.lower())
                    result.append((new_instr, arg))
                    break

    # 4. Pentest synonym replacement
    for instr, arg in phrasings:
        new_instr = _apply_pentest_synonyms(instr)
        if new_instr.lower() != instr.lower() and new_instr.lower() not in seen:
            seen.add(new_instr.lower())
            result.append((new_instr, arg))

    # 5. Question form conversion for statements
    for instr, arg in phrasings:
        if "?" not in instr and len(instr) <= 60:
            # Convert imperative to question
            lower = instr.lower()
            for prefix in ["run ", "execute ", "launch ", "start ", "show ", "display ", "get ", "fetch ", "check ", "verify "]:
                if lower.startswith(prefix):
                    rest = instr[len(prefix):]
                    new_instr = f"How do I {prefix.strip()} {rest}?"
                    if new_instr.lower() not in seen:
                        seen.add(new_instr.lower())
                        result.append((new_instr, arg))
                    new_instr = f"What is the command to {prefix.strip()} {rest}?"
                    if new_instr.lower() not in seen:
                        seen.add(new_instr.lower())
                        result.append((new_instr, arg))
                    break

    # 6. ALL CAPS and lowercase variants for short commands
    for instr, arg in phrasings:
        if len(instr) <= 25 and instr.isalpha():
            new_instr = instr.upper()
            if new_instr.lower() not in seen:
                seen.add(new_instr.lower())
                result.append((new_instr, arg))
            new_instr = instr.lower()
            if new_instr.lower() not in seen:
                seen.add(new_instr.lower())
                result.append((new_instr, arg))

    return result


